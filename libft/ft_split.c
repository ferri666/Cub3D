#include "libft.h"
#include <stdio.h>

static int	ft_count_word(char const *s, char c)
{
	int	i;
	int	word;

	i = 0;
	word = 0;
	while (s && s[i])
	{
		if (s[i] != c)
		{
			word++;
			while (s[i] != c && s[i])
				i++;
		}
		else
			i++;
	}
	return (word);
}

static int	ft_size_word(char const *s, char c, int i)
{
	int	size;

	size = 0;
	while (s[i] != c && s[i])
	{
		size++;
		i++;
	}
	return (size);
}

static void	ft_free(char **strs, int index_strs)
{
	while (index_strs--)
		free(strs[index_strs]);
	free(strs);
}

char	**ft_split(char const *s, char c)
{
	int		i;
	char	**strs;
	int		size;
	int		index_strs;

	i = 0;
	index_strs = -1;
	strs = (char **)malloc((ft_count_word(s, c) + 1) * sizeof(char *));
	if (!strs)
		return (NULL);
	while (++index_strs < ft_count_word(s, c))
	{
		while (s[i] == c)
			i++;
		size = ft_size_word(s, c, i);
		strs[index_strs] = ft_substr(s, i, size);
		if (!strs[index_strs])
		{
			ft_free(strs, index_strs);
			return (NULL);
		}
		i = size + i;
	}
	strs[index_strs] = NULL;
	return (strs);
}
